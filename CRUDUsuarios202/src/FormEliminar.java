    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String idText = txtBuscar.getText();
        if(idText.isEmpty()){
            JOptionPane.showMessageDialog(this,"ID es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        //2. Ejecutamos la consulta y llenamos la tabla 
        try{
            int id = Integer.parseInt(idText);
            ResultSet consulta = crud.obtenerUsuarioPorID(id);
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            
            if(consulta.next()){
                modelo.addRow(new Object[] {consulta.getString("id"), consulta.getString("nombre"), consulta.getString("correo"), consulta.getString("contrasena")} );
            }
        }catch(SQLException b){
            System.out.println("Error al llenar la tabla");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        String id = txtBuscar.getText();
        
        if(!id.isEmpty()){
             int confirmacion = JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar el Usuario", "Actualizar", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION){
                boolean delete = crud.EliminarUsuario(id);
                if (delete){
                JOptionPane.showMessageDialog(this, "Usuario Eliminado", "Exito", JOptionPane.INFORMATION_MESSAGE);
            }
        }else {
                JOptionPane.showMessageDialog(this, "No se Elimino", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }  
    }//GEN-LAST:event_btnEliminarActionPerformed

